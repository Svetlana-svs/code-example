package ru.softwerke.querybuilder.core.data.dto;

public class Database {

    private int id;
    private String name;

    public Database(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

}
